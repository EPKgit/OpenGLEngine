inline float lib::DegToRadf(float degreeMeasure)
{
	return degreeMeasure * (float)M_PI / 180.0f;
}
inline float lib::RadToDegf(float radMeasure)
{
	return radMeasure / (float)M_PI * 180.0f;
}

